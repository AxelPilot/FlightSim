package com.axelsmidt.flightsim;

public class Aircraft {
    double longitude, latitude, altitude;
    double pitch, bank, heading, speed;

    public Aircraft() {
        this.longitude = 0.0;
        this.latitude = 0.0;
        this.altitude = 0.0;
        this.pitch = 0.0;
        this.bank = 0.0;
        this.heading = 0.0;
        this.speed = 0.0;
    }
}
